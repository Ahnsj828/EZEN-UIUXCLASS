const arr = [1, 0, 1, 1, 1, 0, 0, 1, 1, 0];
const solution = (arr) => {
  let answer = 0; // 모든 합계
  let cnt = 0; // 가점

  for (let x of arr) {
    if (x === 1) {
      // 배열에 1이 있으면
      cnt++; // x가 1이면 / 0에서 1이 증가해서 / 증감연산자를 활용해서 1씩더해줄거다
      answer += cnt; // answer에 줄거다
    } else {
      cnt = 0; // x가 1이 아니면 rest시킬거다
    }
  }
  return answer;
};


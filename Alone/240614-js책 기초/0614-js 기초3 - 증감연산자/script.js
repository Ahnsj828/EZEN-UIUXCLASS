let num1 = 10
let num2 = 20
let result
document.write("let num1 = ", num1, "<br/>") // let num1 = 10
document.write("let num2 = ", num2, "<br/>") // let num2 = 20
document.write("let result", "<br/>") // let result
document.write("<br/>")

num1-- // 10 - 1 = 9
document.write("num1--", "<br/>") 
document.write("num1 = ", num1, "<br/>") // num1 = 9

document.write("<br/>")

num1++ // 9 + 1 = 10
document.write("num1--", "<br/>") 
document.write("num1 = ", num1, "<br/>") // num1 = 10

document.write("<br/>")

result = num2++ // result = 20
document.write(result, "<br/>") // 20
document.write("result = ", result, "<br/>") //result = 20
document.write("num2 = ", num2, "<br/>") // num2 = 21

document.write("<br/>")

result = ++num2 // result = 22
document.write(result, "<br/>") // 22
document.write("result = ", result, "<br/>") //result = 22
document.write("num2 = ", num2, "<br/>") // num2 = 22

document.write("<br/>")
